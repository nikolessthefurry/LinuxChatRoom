#include "Socket.h"

#include <cstdint>
#include <array>

namespace Networking
{
class Client
{
  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Construct a new Client object
  /// 
  ////////////////////////////////////////////////////////////////////////////////
  Client();

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Destroy the Client object
  /// 
  ////////////////////////////////////////////////////////////////////////////////
  ~Client();

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Delete the copy constructor.
  /// 
  /// @param[in] Client Client to copy 
  ////////////////////////////////////////////////////////////////////////////////
  Client(const Client& Client) = delete;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Delete the move constructor.
  /// 
  /// @param[in] Client - Client to move 
  ////////////////////////////////////////////////////////////////////////////////
  Client(Client&& Client) = delete;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Delete the copy assignment.
  /// 
  /// @return Client 
  ////////////////////////////////////////////////////////////////////////////////
  Client operator=(const Client&) = delete;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Delete the move assignment
  /// 
  /// @param[in,out] Client Client to copy. 
  /// @return Client& 
  ////////////////////////////////////////////////////////////////////////////////
  Client& operator=(Client&& Client) = delete;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief 
  /// 
  /// @return true 
  /// @return false 
  ////////////////////////////////////////////////////////////////////////////////
  bool connect();

private:

  /// Instance of the socket.
  Socket m_socket;
};
}