#include <sys/socket.h>
#include <cstdint>

namespace Networking
{
class Socket
{
  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Construct a new Socket object
  /// 
  ////////////////////////////////////////////////////////////////////////////////
  Socket(const int32_t family, const int32_t type = SOCK_STREAM, const int32_t protocol = 0);

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Destroy the Socket object
  /// 
  ////////////////////////////////////////////////////////////////////////////////
  ~Socket();

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Delete the copy constructor.
  /// 
  /// @param[in] socket Socket to copy 
  ////////////////////////////////////////////////////////////////////////////////
  Socket(const Socket& socket) = delete;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Delete the move constructor.
  /// 
  /// @param[in] socket - Socket to move 
  ////////////////////////////////////////////////////////////////////////////////
  Socket(Socket&& socket) = delete;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Delete the copy assignment.
  /// 
  /// @return Socket 
  ////////////////////////////////////////////////////////////////////////////////
  Socket operator=(const Socket&) = delete;

  ////////////////////////////////////////////////////////////////////////////////
  /// @brief Delete the move assignment
  /// 
  /// @param[in,out] socket Socket to copy. 
  /// @return Socket& 
  ////////////////////////////////////////////////////////////////////////////////
  Socket& operator=(Socket&& socket) = delete;

private:

  /// Socket File Descriptor Attribute.
  int m_socket_fd;

  /// Is this socket connected.
  bool m_connected;
};
}